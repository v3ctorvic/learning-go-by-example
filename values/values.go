package main

import "fmt"

func main() {

	fmt.Println("go" + "lang") // strings, which can be added to gether with +.

	fmt.Println("1+1 =", 1 + 1) // integers
	fmt.Println("7.0/3.0 =", 7.0/3.0)	// floats

	fmt.Println(true && false) // booleans with boolean operator and "&&".
	fmt.Println(true || false) // booleans with boolean operator or "||".
	fmt.Println(!true) // booolean with the not operator "!".
}
package main

import "fmt"

func main() {

	var a = "initial" // creating a variable and assigning it the value of "initial"
	fmt.Println(a)

	var b, c int = 1, 2 // creating b and c variable and assigning the value 1 and 2
	fmt.Println(b, c)

	var d = true // creating d variable and assigning it the value of "true"
	fmt.Println(d)

	var e int 	// creating the variable e but not assinging a value
	fmt.Println(e)

	f := "apple" // initalizing and assigning variable f to value apple.
	fmt.Println(f)
}